////////////////////////////////////////////////////////////////////////////////
// API classList ///////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//
// La propriété en lecture seule `Element.classList` retourne une collection
// directe `DOMTokenList` des attributs de classe de l'élément.
//
// L'utilisation de `classList` est une alternative à la propriété
// `element.className` qui renvoie une chaine composée de la liste des classes,
// séparées par des espaces.
//
///////////
// SYNTAXE
//
// const elementClasses = elementNodeReference.classList;
//
//////////
// EXEMPLE
//
//
////////////////////////////////////////////////////////////////////////////////
// MÉTHODES ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//
// add( String [, String] )
//
// Ajoute les classes spécifiées. Si une classe est déjà assignée en attribut de
// cet élément, elle est ignorée.
//
//////////
// EXEMPLE
//
//
////////////////////////////////////////////////////////////////////////////////
// remove( String [, String] )
//
// Supprime les classes spécifiées. Note: Supprimer une classe qui n'existe pas
// NE génère PAS d'erreurs.
//
//////////
// EXEMPLE
//
//
////////////////////////////////////////////////////////////////////////////////
// item( Number )
//
// Renvoie la position d'une classe dans une collection.
//
//////////
// EXEMPLE
//
//
////////////////////////////////////////////////////////////////////////////////
// toggle( String [, force] )
//
// Si un seul argument est présent : change la présence d'une classe dans la
// liste. Si la classe existe, alors la supprime et renvoie `false`, dans le cas
// inverse, ajoute cette classe et retourne `true`. Si le second argument est
// présent : Si l'argument `force` est à `true`, ajoute cette classe, si
// l'argument est à `false`, la supprime.
//
//////////
// EXEMPLE
//
//
////////////////////////////////////////////////////////////////////////////////
// contains( String )
//
// Vérifie si la classe spécifiée est présente dans la liste des classes
// attribuées à cet élément.
//
//////////
// EXEMPLE
//
//
////////////////////////////////////////////////////////////////////////////////
// replace( oldClass, newClass )
//
// Remplace une classe par une autre.
//
//////////
// EXEMPLE
//
